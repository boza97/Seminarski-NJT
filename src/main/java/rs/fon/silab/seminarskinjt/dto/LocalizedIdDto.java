package rs.fon.silab.seminarskinjt.dto;

/**
 *
 * @author Bozidar
 */
public class LocalizedIdDto {

    private Long id;
    private String locale;

    public LocalizedIdDto() {
    }

    public LocalizedIdDto(String locale) {
        this.locale = locale;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getLocale() {
        return locale;
    }

    public void setLocale(String locale) {
        this.locale = locale;
    }

}
